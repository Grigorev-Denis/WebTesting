package org.example;

public class Boxes {
    private double width;
    private double heigth;
    private double depth;

    public Boxes(double width, double heigth, double depth) {
        this.width = width;
        this.heigth = heigth;
        this.depth = depth;
    }

    public double getWidth() {
        return width;
    }

    public double getHeigth() {
        return heigth;
    }

    public double getDepth() {
        return depth;
    }
}